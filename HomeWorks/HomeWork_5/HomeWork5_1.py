def gcd(x, y: int) -> str:
    """
    Функція для знаходження найбільшого спільного дільника (НСД) двох чисел
    за допомогою алгоритму Евкліда.
    :param x: Перше ціле число.
    :param y: Друге ціле число.
    :return: Найбільший спільний дільник двох чисел.
    """
    if x == 0:
        return y
    elif y == 0:
        return x
    else:
        return gcd(y, x % y)


x = 0
y = 18
print("Найбільший спільний дільник:", gcd(x, y))
